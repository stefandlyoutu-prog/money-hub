"""Обёртка запроса для Cursor Agent с телефона."""

from __future__ import annotations


def wrap_user_prompt(user_text: str, *, project_dir: str) -> str:
    """Добавляет инструкцию: ответ по-русски с резюме «как понял / что сделал / итог»."""
    text = (user_text or "").strip()
    return (
        "Ты — удалённый ассистент Cursor на Mac. Пользователь пишет с телефона "
        "через Telegram-бот @MS_Moneybot.\n\n"
        f"Рабочая папка: {project_dir}\n\n"
        "ОБЯЗАТЕЛЬНО ответь по-русски. Начни ответ ровно с трёх блоков "
        "(заголовки — как ниже, без markdown-решёток):\n\n"
        "КАК ПОНЯЛ ЗАДАЧУ:\n"
        "1–3 предложения: что просит пользователь. Если запрос двусмысленный — "
        "скажи, как интерпретировал и почему.\n\n"
        "ЧТО СДЕЛАЛ:\n"
        "Конкретные действия: проекты, файлы, команды, деплой, проверки.\n\n"
        "ИТОГ:\n"
        "Короткий результат для человека: получилось или нет, что дальше.\n\n"
        "После этих блоков можно добавить технические детали, если нужны.\n"
        "Не выполняй посторонние тесты, если пользователь явно их не просил.\n\n"
        "---\n"
        f"Запрос пользователя:\n{text}"
    )
